package chapter_10.exampl;
/*
Приведен простой пример программы, в которой метод
write () используется для вывода символа s и символа
перевода строки на консоль.
 */
public class WriteDemo {
    public static void main(String[] args) {
        int b;
        b = 'S';
        System.out.write(b);
        System.out.write('\n');
    }
}
